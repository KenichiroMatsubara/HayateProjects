#!/usr/bin/env node
// gen-from-wit.mjs — wit/hayate.wit を単一ソースとして、両言語の判別子定義を生成する。
//
// 生成物:
//   Hayate/crates/adapters/web/src/wit_generated.rs   (Rust: wasm-bindgen 公開 + pub(crate) const)
//   Tsubame/packages/renderer-canvas/src/opcodes.ts   (TypeScript: OP / ELEMENT_KIND / EVENT)
//
// op_kind / element_kind / event_kind / modifier-keys が WIT・Rust・TS の三者で
// 食い違わないことを構造的に保証する。手書き禁止：判別子を増やすときは WIT を
// 編集して本スクリプトを再実行する。CI は `node gen-from-wit.mjs && git diff
// --exit-code` で stale を検出する。
//
// WIT は外部ツール（wit-bindgen 等）に依存せず、enum / flags / variant の
// 単純な構文だけを正規表現で読む軽量パーサで処理する。

import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const HERE = dirname(fileURLToPath(import.meta.url));
const WIT_PATH = resolve(HERE, '../wit/hayate.wit');
const RUST_OUT = resolve(HERE, '../crates/adapters/web/src/wit_generated.rs');
const TS_OUT = resolve(HERE, '../../Tsubame/packages/renderer-canvas/src/opcodes.ts');

// ── WIT パーサ ─────────────────────────────────────────────────────────────
// `//` 行コメントを除去してから `keyword name { members }` ブロックを取り出す。
// メンバは `,` 区切り、`name(payload)` の payload は捨てて name だけを残す。

const WIT_SRC = readFileSync(WIT_PATH, 'utf8');
const WIT_NO_COMMENTS = WIT_SRC.replace(/\/\/.*$/gm, '');

function parseBlock(keyword, name) {
  const re = new RegExp(`${keyword}\\s+${name}\\s*\\{([^}]*)\\}`, 's');
  const m = WIT_NO_COMMENTS.match(re);
  if (!m) throw new Error(`WIT block not found: ${keyword} ${name}`);
  return m[1]
    .split(',')
    .map((s) => s.trim())
    .filter(Boolean)
    .map((s) => s.replace(/\(.*$/s, '').trim())
    .filter(Boolean);
}

const ops = parseBlock('enum', 'op-kind');
const kinds = parseBlock('enum', 'element-kind');
const events = parseBlock('variant', 'event');
const modifiers = parseBlock('flags', 'modifier-keys');

// ── 命名変換 ─────────────────────────────────────────────────────────────
const SCREAM = (s) => s.toUpperCase().replace(/-/g, '_'); // append-child → APPEND_CHILD
const snake = (s) => s.replace(/-/g, '_'); // append-child → append_child
const tsKey = (s) => (/-/.test(s) ? `'${s}'` : s); // text-input → 'text-input'

const RUST_HEADER = `// @generated by Hayate/scripts/gen-from-wit.mjs from wit/hayate.wit — DO NOT EDIT.
//
// op_kind / element_kind / event_kind / modifier-keys の判別子は wit/hayate.wit が
// 単一ソース。手書きで編集せず、WIT を変えて \`node scripts/gen-from-wit.mjs\` を
// 再実行すること。

use wasm_bindgen::prelude::*;
`;

const TS_HEADER = `// @generated by Hayate/scripts/gen-from-wit.mjs from Hayate/wit/hayate.wit — DO NOT EDIT.
//
// OP / ELEMENT_KIND / EVENT は Hayate の wit/hayate.wit が単一ソース。手書きで
// 編集せず、WIT を変えて Hayate 側で \`node scripts/gen-from-wit.mjs\` を再実行する。
import type { ElementKind } from '@tsubame/renderer-protocol';
`;

// ── Rust 生成 ─────────────────────────────────────────────────────────────
function genRust() {
  let out = RUST_HEADER;

  out += `\n// ── op-kind: apply_mutations の op_kind 判別子（ADR-0039）──\n`;
  ops.forEach((m, i) => {
    out += `pub(crate) const OP_${SCREAM(m)}: u32 = ${i};\n`;
  });
  out += `\n`;
  ops.forEach((m) => {
    out += `#[wasm_bindgen]\npub fn op_kind_${snake(m)}() -> u32 {\n    OP_${SCREAM(m)}\n}\n`;
  });

  out += `\n// ── element-kind: kind_code（ElementKind の discriminant と一致）──\n`;
  kinds.forEach((m, i) => {
    out += `#[wasm_bindgen]\npub fn element_kind_${snake(m)}() -> u32 {\n    ${i}\n}\n`;
  });

  out += `\n// ── event: poll_events() の wire 判別子（encode_events が参照）──\n`;
  events.forEach((m, i) => {
    out += `pub(crate) const EVENT_${SCREAM(m)}: f64 = ${i}.0;\n`;
  });
  out += `\n`;
  events.forEach((m) => {
    out += `#[wasm_bindgen]\npub fn event_kind_${snake(m)}() -> f64 {\n    EVENT_${SCREAM(m)}\n}\n`;
  });

  out += `\n// ── modifier-keys: KeyboardEvent 修飾キーのビットマスク ──\n`;
  modifiers.forEach((m, i) => {
    out += `#[wasm_bindgen]\npub fn modifier_${snake(m)}() -> u32 {\n    ${1 << i}\n}\n`;
  });

  return out;
}

// ── TypeScript 生成 ───────────────────────────────────────────────────────
function genTs() {
  let out = TS_HEADER;

  out += `\n/**\n * \`apply_mutations\` の op_kind コード。Hayate の wasm が公開する\n * \`op_kind_*()\` と 1:1 で一致する（同じ wit/hayate.wit から生成）。\n */\nexport const OP = {\n`;
  ops.forEach((m, i) => {
    out += `  ${SCREAM(m)}: ${i},\n`;
  });
  out += `} as const;\n`;

  out += `\n/**\n * \`ElementKind\` → kind_code。Hayate の \`element_kind_*()\` と一致。\n * \`OP.CREATE\` の operand として ops ストリームに乗せる。\n */\nexport const ELEMENT_KIND: Record<ElementKind, number> = {\n`;
  kinds.forEach((m, i) => {
    out += `  ${tsKey(m)}: ${i},\n`;
  });
  out += `};\n`;

  out += `\n/**\n * \`poll_events()\` が返す各イベントの先頭判別子コード。Hayate の\n * \`event_kind_*()\` および encode_events の wire 値と一致する。\n */\nexport const EVENT = {\n`;
  events.forEach((m, i) => {
    out += `  ${SCREAM(m)}: ${i},\n`;
  });
  out += `} as const;\n`;

  return out;
}

writeFileSync(RUST_OUT, genRust());
writeFileSync(TS_OUT, genTs());

console.log(`✓ wit codegen done`);
console.log(`  op-kind     : ${ops.length} members`);
console.log(`  element-kind: ${kinds.length} members`);
console.log(`  event       : ${events.length} members`);
console.log(`  modifier    : ${modifiers.length} members`);
console.log(`  → ${RUST_OUT}`);
console.log(`  → ${TS_OUT}`);
