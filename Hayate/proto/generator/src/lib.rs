/// Flatten style tag params: expand compound types (color, dimension) into raw f32 fields.
fn flatten_tag_params(params: &[Param], proto: &Proto) -> Vec<(String, String)> {
    let mut result = Vec::new();
    for p in params {
        if p.typ == "i32" {
            // i32 values (e.g. z-index) occupy one f32 wire slot
            result.push((p.name.clone(), "f32".to_string()));
            continue;
        }
        // Check if this param type matches a known compound type
        if let Some(td) = proto.types.iter().find(|t| t.name == p.typ) {
            // Expand fields
            for f in &td.fields {
                let fname = format!("{}_{}", p.name, f.name);
                result.push((fname, "f32".to_string()));
            }
        } else if p.typ == "string" {
            result.push((p.name.clone(), "String".to_string()));
        } else {
            // f32, display enums, etc. — all stored as f32
            result.push((p.name.clone(), "f32".to_string()));
        }
    }
    result
}

fn event_match_pattern(ev: &Entry, qualified: bool) -> String {
    let variant = to_pascal(&ev.name);
    let prefix = if qualified { "hayate_core::Event::" } else { "Event::" };
    if ev.params.is_empty() {
        format!("{}{}", prefix, variant)
    } else {
        let fields: Vec<String> = ev.params.iter().map(|p| p.name.clone()).collect();
        format!("{}{} {{ {} }}", prefix, variant, fields.join(", "))
    }
}

fn event_match_wildcard(ev: &Entry) -> String {
    let variant = to_pascal(&ev.name);
    if ev.params.is_empty() {
        format!("Event::{}", variant)
    } else {
        format!("Event::{} {{ .. }}", variant)
    }
}

fn encode_event_wire_param_push(param: &Param, field: &str) -> String {
    match param.typ.as_str() {
        "element_id" => format!(
            "            out.push(EventWireValue::Number({}.to_u64() as f64));\n",
            field
        ),
        "string" => format!(
            "            out.push(EventWireValue::Text({}.clone()));\n",
            field
        ),
        "f32" | "f64" | "u32" | "usize" | "bool" => {
            format!("            out.push(EventWireValue::Number(*{} as f64));\n", field)
        }
        other => panic!("unsupported event param type: {other}"),
    }
}

fn generate_encode_event(proto: &Proto) -> String {
    let mut out = String::new();

    out.push_str("#[derive(Debug, Clone, PartialEq)]\n");
    out.push_str("pub enum EventWireValue {\n");
    out.push_str("    Number(f64),\n");
    out.push_str("    Text(String),\n");
    out.push_str("}\n\n");

    out.push_str("pub fn encode_event_wire(ev: &hayate_core::Event) -> Vec<EventWireValue> {\n");
    out.push_str("    match ev {\n");

    for ev in &proto.event_kinds {
        out.push_str(&format!("        {} => {{\n", event_match_pattern(ev, true)));
        out.push_str("            let mut out = Vec::new();\n");
        out.push_str(&format!(
            "            out.push(EventWireValue::Number({}.0));\n",
            ev.value
        ));
        for p in &ev.params {
            out.push_str(&encode_event_wire_param_push(p, &p.name));
        }
        out.push_str("            out\n");
        out.push_str("        }\n");
    }

    out.push_str("    }\n");
    out.push_str("}\n\n");

    out.push_str("pub fn encode_event(ev: &hayate_core::Event) -> js_sys::Array {\n");
    out.push_str("    use wasm_bindgen::JsValue;\n");
    out.push_str("    let sub = js_sys::Array::new();\n");
    out.push_str("    for atom in encode_event_wire(ev) {\n");
    out.push_str("        match atom {\n");
    out.push_str("            EventWireValue::Number(n) => { sub.push(&JsValue::from_f64(n)); }\n");
    out.push_str("            EventWireValue::Text(s) => { sub.push(&JsValue::from_str(&s)); }\n");
    out.push_str("        }\n");
    out.push_str("    }\n");
    out.push_str("    sub\n");
    out.push_str("}\n\n");

    out.push_str("pub fn encode_events(events: &[hayate_core::Event]) -> js_sys::Array {\n");
    out.push_str("    let result = js_sys::Array::new();\n");
    out.push_str("    for ev in events {\n");
    out.push_str("        result.push(&encode_event(ev));\n");
    out.push_str("    }\n");
    out.push_str("    result\n");
    out.push_str("}\n\n");

    out.push_str(
        "pub fn encode_deliveries(deliveries: &[hayate_core::EventDelivery]) -> js_sys::Array {\n",
    );
    out.push_str("    let result = js_sys::Array::new();\n");
    out.push_str("    for delivery in deliveries {\n");
    out.push_str("        let sub = encode_event(&delivery.event);\n");
    out.push_str("        let row = js_sys::Array::new();\n");
    out.push_str("        row.push(&JsValue::from_f64(delivery.listener_id.to_u64() as f64));\n");
    out.push_str("        for i in 0..sub.length() {\n");
    out.push_str("            row.push(&sub.get(i));\n");
    out.push_str("        }\n");
    out.push_str("        result.push(&row);\n");
    out.push_str("    }\n");
    out.push_str("    result\n");
    out.push_str("}\n");

    out
}

