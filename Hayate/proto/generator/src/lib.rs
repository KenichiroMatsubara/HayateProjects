/// Element-kind tables consumed by `hayate_core` (`element/kind.rs`). The single
/// source for the UA default cursor per element-kind (ADR-0105): Canvas resolves
/// it through `resolve_cursor`, the DOM renderer reads the parallel TS table, and
/// neither renderer re-declares the mapping.
fn generate_element_kind_tables(proto: &Proto) -> String {
    let mut out = String::new();
    out.push_str(GENERATED_HEADER);
    out.push_str("use super::{CursorValue, ElementKind, UserSelectValue};\n\n");

    out.push_str(
        "/// UA default cursor for `kind` when no explicit `cursor` is set (ADR-0105).\n",
    );
    out.push_str("pub fn default_cursor(kind: ElementKind) -> CursorValue {\n");
    out.push_str("    match kind {\n");
    for ek in &proto.element_kinds {
        if let Some(cursor) = &ek.default_cursor {
            out.push_str(&format!(
                "        ElementKind::{} => CursorValue::{},\n",
                to_pascal(&ek.name),
                to_pascal(cursor)
            ));
        }
    }
    out.push_str("        _ => CursorValue::Default,\n");
    out.push_str("    }\n");
    out.push_str("}\n\n");

    out.push_str(
        "/// UA default `user-select` for `kind` when no explicit `user-select` is set\n\
         /// (ADR-0108). Single source for Canvas/DOM kind-default selectability.\n",
    );
    out.push_str("pub fn default_user_select(kind: ElementKind) -> UserSelectValue {\n");
    out.push_str("    match kind {\n");
    for ek in &proto.element_kinds {
        if let Some(user_select) = &ek.default_user_select {
            out.push_str(&format!(
                "        ElementKind::{} => UserSelectValue::{},\n",
                to_pascal(&ek.name),
                to_pascal(user_select)
            ));
        }
    }
    out.push_str("        _ => UserSelectValue::None,\n");
    out.push_str("    }\n");
    out.push_str("}\n\n");

    let editable: Vec<String> = proto
        .element_kinds
        .iter()
        .filter(|k| k.accepts_text_input)
        .map(|k| format!("ElementKind::{}", to_pascal(&k.name)))
        .collect();
    out.push_str(
        "/// Whether `kind` accepts text entry and so surfaces the platform soft\n\
         /// keyboard / IME when focused (#392).\n",
    );
    out.push_str("pub fn accepts_text_input(kind: ElementKind) -> bool {\n");
    if editable.is_empty() {
        out.push_str("    let _ = kind;\n    false\n");
    } else {
        out.push_str("    matches!(\n        kind,\n        ");
        out.push_str(&editable.join("\n            | "));
        out.push_str("\n    )\n");
    }
    out.push_str("}\n");

    out
}

