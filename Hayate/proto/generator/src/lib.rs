/// Flatten style tag params: expand compound types (color, dimension) into raw f32 fields.
fn flatten_tag_params(params: &[Param], proto: &Proto) -> Vec<(String, String)> {
    let mut result = Vec::new();
    for p in params {
        if p.typ == "i32" {
            // i32 values (e.g. z-index) occupy one f32 wire slot
            result.push((p.name.clone(), "f32".to_string()));
            continue;
        }
        // Check if this param type matches a known compound type
        if let Some(td) = proto.types.iter().find(|t| t.name == p.typ) {
            // Expand fields
            for f in &td.fields {
                let fname = format!("{}_{}", p.name, f.name);
                result.push((fname, "f32".to_string()));
            }
        } else if p.typ == "string" {
            result.push((p.name.clone(), "String".to_string()));
        } else {
            // f32, display enums, etc. — all stored as f32
            result.push((p.name.clone(), "f32".to_string()));
        }
    }
    result
}

fn event_match_pattern(ev: &Entry, qualified: bool) -> String {
    let variant = to_pascal(&ev.name);
    let prefix = if qualified { "hayate_core::Event::" } else { "Event::" };
    if ev.params.is_empty() {
        format!("{}{}", prefix, variant)
    } else {
        let fields: Vec<String> = ev.params.iter().map(|p| p.name.clone()).collect();
        format!("{}{} {{ {} }}", prefix, variant, fields.join(", "))
    }
}

fn event_match_wildcard(ev: &Entry) -> String {
    let variant = to_pascal(&ev.name);
    if ev.params.is_empty() {
        format!("Event::{}", variant)
    } else {
        format!("Event::{} {{ .. }}", variant)
    }
}

fn encode_event_wire_param_push(param: &Param, field: &str) -> String {
    match param.typ.as_str() {
        "element_id" => format!(
            "            out.push(EventWireValue::Number({}.to_u64() as f64));\n",
            field
        ),
        "string" => format!(
            "            out.push(EventWireValue::Text({}.clone()));\n",
            field
        ),
        "f32" | "f64" | "u32" | "usize" | "bool" => {
            format!("            out.push(EventWireValue::Number(*{} as f64));\n", field)
        }
        "pointer_kind" => format!(
            "            out.push(EventWireValue::Number({}.to_u32() as f64));\n",
            field
        ),
        other => panic!("unsupported event param type: {other}"),
    }
}

fn generate_encode_event_wire(proto: &Proto) -> String {
    let mut out = String::new();

    out.push_str("#[derive(Debug, Clone, PartialEq)]\n");
    out.push_str("pub enum EventWireValue {\n");
    out.push_str("    Number(f64),\n");
    out.push_str("    Text(String),\n");
    out.push_str("}\n\n");

    out.push_str("pub fn encode_event_wire(ev: &hayate_core::Event) -> Vec<EventWireValue> {\n");
    out.push_str("    match ev {\n");

    for ev in &proto.event_kinds {
        out.push_str(&format!("        {} => {{\n", event_match_pattern(ev, true)));
        out.push_str("            let mut out = Vec::new();\n");
        out.push_str(&format!(
            "            out.push(EventWireValue::Number({}.0));\n",
            ev.value
        ));
        for p in &ev.params {
            out.push_str(&encode_event_wire_param_push(p, &p.name));
        }
        out.push_str("            out\n");
        out.push_str("        }\n");
    }

    out.push_str("    }\n");
    out.push_str("}\n\n");

    out
}

/// 生成: `js_sys::Array` ベースのイベントエンコーダ（Web 専用 / ADR-0112）。中立な
/// `encode_event_wire` を JS 配列行へ持ち上げる。wasm-bindgen / js_sys に依存するため
/// 専用ファイル `event_encode_web.rs` へ書き出し、Android からは include しない。
fn generate_encode_event_js(_proto: &Proto) -> String {
    let mut out = String::new();
    out.push_str(GENERATED_HEADER);
    out.push_str("// ── Event encode (js_sys, Web 専用) ─────────────────────────────────────\n\n");

    out.push_str("pub fn encode_event(ev: &hayate_core::Event) -> js_sys::Array {\n");
    out.push_str("    use wasm_bindgen::JsValue;\n");
    out.push_str("    let sub = js_sys::Array::new();\n");
    out.push_str("    for atom in encode_event_wire(ev) {\n");
    out.push_str("        match atom {\n");
    out.push_str("            EventWireValue::Number(n) => { sub.push(&JsValue::from_f64(n)); }\n");
    out.push_str("            EventWireValue::Text(s) => { sub.push(&JsValue::from_str(&s)); }\n");
    out.push_str("        }\n");
    out.push_str("    }\n");
    out.push_str("    sub\n");
    out.push_str("}\n\n");

    out.push_str("pub fn encode_events(events: &[hayate_core::Event]) -> js_sys::Array {\n");
    out.push_str("    let result = js_sys::Array::new();\n");
    out.push_str("    for ev in events {\n");
    out.push_str("        result.push(&encode_event(ev));\n");
    out.push_str("    }\n");
    out.push_str("    result\n");
    out.push_str("}\n\n");

    out.push_str(
        "pub fn encode_deliveries(deliveries: &[hayate_core::EventDelivery]) -> js_sys::Array {\n",
    );
    out.push_str("    let result = js_sys::Array::new();\n");
    out.push_str("    for delivery in deliveries {\n");
    out.push_str("        let sub = encode_event(&delivery.event);\n");
    out.push_str("        let row = js_sys::Array::new();\n");
    out.push_str("        row.push(&JsValue::from_f64(delivery.listener_id.to_u64() as f64));\n");
    out.push_str("        for i in 0..sub.length() {\n");
    out.push_str("            row.push(&sub.get(i));\n");
    out.push_str("        }\n");
    out.push_str("        result.push(&row);\n");
    out.push_str("    }\n");
    out.push_str("    result\n");
    out.push_str("}\n");

    out
}

fn event_field_rust_type(typ: &str) -> String {
    match typ {
        "element_id" => "ElementId".to_string(),
        "string" => "String".to_string(),
        "f32" => "f32".to_string(),
        "f64" => "f64".to_string(),
        "u32" => "u32".to_string(),
        "usize" => "usize".to_string(),
        "bool" => "bool".to_string(),
        "pointer_kind" => "PointerKind".to_string(),
        other => panic!("unsupported event param type: {other}"),
    }
}

fn generate_event_types(proto: &Proto) -> String {
    let mut out = String::new();
    out.push_str(GENERATED_HEADER);
    out.push_str("use super::id::ElementId;\n");
    // Pull in any enum types referenced by event params (e.g. PointerKind on
    // pointer_move). Only emitted when an event actually uses the type so the
    // generated `use` never dangles.
    if proto
        .event_kinds
        .iter()
        .any(|ev| ev.params.iter().any(|p| p.typ == "pointer_kind"))
    {
        out.push_str("use super::pointer::PointerKind;\n");
    }
    out.push('\n');

    out.push_str("/// Semantic interaction events (ADR-0031).\n");
    out.push_str("#[derive(Clone, Debug)]\n");
    out.push_str("pub enum Event {\n");
    for ev in &proto.event_kinds {
        if let Some(desc) = &ev.description {
            for line in desc.lines() {
                out.push_str(&format!("    /// {}\n", line));
            }
        }
        let variant = to_pascal(&ev.name);
        if ev.params.is_empty() {
            out.push_str(&format!("    {},\n", variant));
        } else {
            out.push_str(&format!("    {} {{\n", variant));
            for p in &ev.params {
                out.push_str(&format!(
                    "        {}: {},\n",
                    p.name,
                    event_field_rust_type(&p.typ)
                ));
            }
            out.push_str("    },\n");
        }
    }
    out.push_str("}\n\n");

    let doc_events: Vec<_> = proto
        .event_kinds
        .iter()
        .filter(|ev| ev.document_runtime)
        .collect();

    out.push_str("/// Event kinds registerable via `register_listener`.\n");
    out.push_str("#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]\n");
    out.push_str("pub enum DocumentEventKind {\n");
    for ev in &doc_events {
        out.push_str(&format!("    {},\n", to_pascal(&ev.name)));
    }
    out.push_str("}\n\n");

    out.push_str("impl DocumentEventKind {\n");
    out.push_str("    pub fn bubbles(self) -> bool {\n");
    out.push_str("        match self {\n");
    for ev in &doc_events {
        let arm = if ev.bubbles { "true" } else { "false" };
        out.push_str(&format!(
            "            Self::{} => {},\n",
            to_pascal(&ev.name),
            arm
        ));
    }
    out.push_str("        }\n");
    out.push_str("    }\n\n");

    out.push_str("    pub fn from_u32(value: u32) -> Option<Self> {\n");
    out.push_str("        match value {\n");
    for ev in &doc_events {
        out.push_str(&format!(
            "            {} => Some(Self::{}),\n",
            ev.value,
            to_pascal(&ev.name)
        ));
    }
    out.push_str("            _ => None,\n");
    out.push_str("        }\n");
    out.push_str("    }\n");
    out.push_str("}\n\n");

    out.push_str("/// Map a runtime `Event` to its document kind, if registerable.\n");
    out.push_str("pub fn event_document_kind(event: &Event) -> Option<DocumentEventKind> {\n");
    out.push_str("    match event {\n");
    for ev in &doc_events {
        out.push_str(&format!(
            "        {} => Some(DocumentEventKind::{}),\n",
            event_match_wildcard(ev),
            to_pascal(&ev.name)
        ));
    }
    out.push_str("        _ => None,\n");
    out.push_str("    }\n");
    out.push_str("}\n");

    out
}

