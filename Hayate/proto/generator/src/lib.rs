#[derive(Default)]
struct Proto {
    types: Vec<TypeDef>,
    enums: Vec<EnumDef>,
    opcodes: Vec<Entry>,
    style_tags: Vec<Entry>,
    event_kinds: Vec<Entry>,
    element_kinds: Vec<SimpleEntry>,
    unset_kinds: Vec<SimpleEntry>,
    modifier_keys: Vec<SimpleEntry>,
    pseudo_states: Vec<PseudoStateEntry>,
}

#[derive(Default)]
struct TypeDef {
    name: String,
    #[allow(dead_code)]
    raw_slots: usize,
    fields: Vec<Param>,
}

#[derive(Default)]
struct EnumDef {
    name: String,
    #[allow(dead_code)]
    string_values: bool,
    values: Vec<EnumValue>,
}

#[derive(Default, Clone)]
struct EnumValue {
    name: String,
    value: String, // numeric string or quoted string
}

#[derive(Default)]
struct Entry {
    name: String,
    value: u32,
    variable_length: bool,
    params: Vec<Param>,
    #[allow(dead_code)]
    encode_from: Option<String>,
    wire_role: Option<String>,
    adapter_tier: Option<String>,
    document_runtime: bool,
    bubbles: bool,
    #[allow(dead_code)]
    interaction_kind: Option<String>,
    description: Option<String>,
    dom_css: Option<DomCss>,
}

#[derive(Default, Clone)]
struct DomCss {
    property: String,
    format: String,
    extras: Vec<DomExtra>,
}

#[derive(Default, Clone)]
struct DomExtra {
    property: String,
    when_positive: String,
    when_zero: String,
}

#[derive(Default, Clone)]
struct Param {
    name: String,
    typ: String,
    count: usize, // 0 means 1 (no count attribute)
}

#[derive(Default)]
struct SimpleEntry {
    name: String,
    value: u32,
}

#[derive(Default)]
struct PseudoStateEntry {
    name: String,
    value: u32,
    priority: u32,
    css_pseudo: String,
}

