fn param_slots(p: &Param) -> usize {
    let count = if p.count == 0 { 1 } else { p.count };
    count
}

fn param_rust_type(typ: &str, count: usize) -> String {
    let base = match typ {
        "element_id" => "u64",
        "u32" => "u32",
        "f32" => "f32",
        "f64" => "f64",
        "bool" => "bool",
        "usize" => "usize",
        "string" => "String",
        other => other, // enums etc. – not used in Op/StyleTag
    };
    if count > 1 {
        format!("[{}; {}]", base, count)
    } else {
        base.to_string()
    }
}

fn generate(proto: &Proto) -> String {
    let mut out = String::new();

    out.push_str(GENERATED_HEADER);

    // OP_* constants
    out.push_str("// Opcode constants\n");
    for op in &proto.opcodes {
        out.push_str(&format!(
            "pub const OP_{}: u32 = {};\n",
            op.name, op.value
        ));
    }
    out.push('\n');

    // OP_SLOTS
    out.push_str("// Payload slot counts per opcode (op discriminant excluded)\n");
    out.push_str("pub const OP_SLOTS: &[usize] = &[\n");
    for op in &proto.opcodes {
        let slots: usize = op.params.iter().map(param_slots).sum();
        out.push_str(&format!(
            "    {}, // {}\n",
            slots, op.name
        ));
    }
    out.push_str("];\n\n");

    // TAG_* constants
    out.push_str("// Style tag constants\n");
    for tag in &proto.style_tags {
        out.push_str(&format!(
            "pub const TAG_{}: u32 = {};\n",
            tag.name, tag.value
        ));
    }
    out.push('\n');

    // EVENT_KIND_* constants (f64)
    out.push_str("// Event kind constants\n");
    for ev in &proto.event_kinds {
        out.push_str(&format!(
            "pub const EVENT_KIND_{}: f64 = {}.0;\n",
            ev.name.to_uppercase(),
            ev.value
        ));
    }
    out.push('\n');

    out.push_str("// Event wire metadata (from event_kinds.json)\n");
    for ev in &proto.event_kinds {
        let wire_role = ev
            .wire_role
            .as_deref()
            .unwrap_or_else(|| panic!("event_kinds.{}: missing wireRole", ev.name));
        let adapter_tier = ev
            .adapter_tier
            .as_deref()
            .unwrap_or_else(|| panic!("event_kinds.{}: missing adapterTier", ev.name));
        out.push_str(&format!(
            "pub const EVENT_WIRE_ROLE_{}: &str = \"{}\";\n",
            ev.name.to_uppercase(),
            wire_role
        ));
        out.push_str(&format!(
            "pub const EVENT_ADAPTER_TIER_{}: &str = \"{}\";\n",
            ev.name.to_uppercase(),
            adapter_tier
        ));
    }
    out.push('\n');

    // ELEMENT_KIND_* constants
    out.push_str("// Element kind constants\n");
    for ek in &proto.element_kinds {
        out.push_str(&format!(
            "pub const ELEMENT_KIND_{}: u32 = {};\n",
            ek.name.to_uppercase(),
            ek.value
        ));
    }
    out.push('\n');

    // UNSET_KIND_* constants
    out.push_str("// Unset kind constants\n");
    for uk in &proto.unset_kinds {
        out.push_str(&format!(
            "pub const UNSET_KIND_{}: u32 = {};\n",
            uk.name.to_uppercase(),
            uk.value
        ));
    }
    out.push('\n');

    // MODIFIER_* constants
    out.push_str("// Modifier key constants\n");
    for mk in &proto.modifier_keys {
        out.push_str(&format!(
            "pub const MODIFIER_{}: u32 = {};\n",
            mk.name.to_uppercase(),
            mk.value
        ));
    }
    out.push('\n');

    // Op enum
    out.push_str("// Op enum\n");
    out.push_str("#[derive(Debug, Clone)]\n");
    out.push_str("pub enum Op {\n");
    for op in &proto.opcodes {
        // Convert SNAKE_UPPER to PascalCase
        let variant = to_pascal(&op.name);
        if op.params.is_empty() {
            out.push_str(&format!("    {},\n", variant));
        } else {
            out.push_str(&format!("    {} {{\n", variant));
            for p in &op.params {
                let count = if p.count == 0 { 1 } else { p.count };
                let rust_ty = param_rust_type(&p.typ, count);
                out.push_str(&format!("        {}: {},\n", p.name, rust_ty));
            }
            out.push_str("    },\n");
        }
    }
    out.push_str("}\n\n");

    // parse_next_op
    out.push_str("pub fn parse_next_op(ops: &[f64], i: usize) -> Result<(Op, usize), &'static str> {\n");
    out.push_str("    if i >= ops.len() { return Err(\"unexpected end of ops\"); }\n");
    out.push_str("    let disc = ops[i] as u32;\n");
    out.push_str("    let i = i + 1;\n");
    out.push_str("    match disc {\n");
    for op in &proto.opcodes {
        let variant = to_pascal(&op.name);
        let total_slots: usize = op.params.iter().map(param_slots).sum();
        out.push_str(&format!("        {} => {{\n", op.value));
        out.push_str(&format!(
            "            if i + {} > ops.len() {{ return Err(\"op {} truncated\"); }}\n",
            total_slots, op.name
        ));
        let mut slot = 0usize;
        for p in &op.params {
            let count = if p.count == 0 { 1 } else { p.count };
            if count > 1 {
                out.push_str(&format!("            let mut {} = [0f64; {}];\n", p.name, count));
                for ci in 0..count {
                    out.push_str(&format!(
                        "            {}[{}] = ops[i + {}];\n",
                        p.name,
                        ci,
                        slot + ci
                    ));
                }
            } else {
                let cast = match p.typ.as_str() {
                    "element_id" => "ops[i + IDX] as u64",
                    "u32" => "ops[i + IDX] as u32",
                    "f32" => "ops[i + IDX] as f32",
                    "f64" => "ops[i + IDX]",
                    "bool" => "ops[i + IDX] != 0.0",
                    "usize" => "ops[i + IDX] as usize",
                    _ => "ops[i + IDX] as u64",
                };
                let cast = cast.replace("IDX", &slot.to_string());
                out.push_str(&format!("            let {} = {};\n", p.name, cast));
            }
            slot += count;
        }
        if op.params.is_empty() {
            out.push_str(&format!("            Ok((Op::{}, i))\n", variant));
        } else {
            let field_list: Vec<String> = op.params.iter().map(|p| p.name.clone()).collect();
            out.push_str(&format!(
                "            Ok((Op::{} {{ {} }}, i + {}))\n",
                variant,
                field_list.join(", "),
                total_slots
            ));
        }
        out.push_str("        }\n");
    }
    out.push_str("        _ => Err(\"unknown opcode\"),\n");
    out.push_str("    }\n");
    out.push_str("}\n\n");

    // StyleTag enum
    out.push_str("// StyleTag enum\n");
    out.push_str("#[derive(Debug, Clone)]\n");
    out.push_str("pub enum StyleTag {\n");
    for tag in &proto.style_tags {
        let variant = to_pascal(&tag.name);
        if ValueType::classify(tag).is_dimension_list() {
            out.push_str(&format!("    {} {{\n", variant));
            out.push_str("        tracks: Vec<(f32, f32)>,\n");
            out.push_str("    },\n");
        } else if tag.params.is_empty() {
            out.push_str(&format!("    {},\n", variant));
        } else {
            // flatten compound types to raw f32
            let fields = flatten_tag_params(&tag.params, proto);
            if fields.is_empty() {
                out.push_str(&format!("    {},\n", variant));
            } else {
                out.push_str(&format!("    {} {{\n", variant));
                for (fname, ftype) in &fields {
                    out.push_str(&format!("        {}: {},\n", fname, ftype));
                }
                out.push_str("    },\n");
            }
        }
    }
    out.push_str("}\n\n");

    // parse_next_style_tag
    out.push_str("pub fn parse_next_style_tag(packed: &[f32], i: usize) -> Result<(StyleTag, usize), &'static str> {\n");
    out.push_str("    if i >= packed.len() { return Err(\"unexpected end of style data\"); }\n");
    out.push_str("    let tag = packed[i] as u32;\n");
    out.push_str("    let i = i + 1;\n");
    out.push_str("    match tag {\n");
    for tag in &proto.style_tags {
        let variant = to_pascal(&tag.name);
        let fields = flatten_tag_params(&tag.params, proto);
        let slot_count = fields.len();
        out.push_str(&format!("        {} => {{\n", tag.value));
        if tag.variable_length && ValueType::classify(tag).is_dimension_list() {
            out.push_str("            if i >= packed.len() { return Err(\"style tag track list truncated\"); }\n");
            out.push_str("            let track_count = packed[i] as usize;\n");
            out.push_str("            let mut tracks = Vec::with_capacity(track_count);\n");
            out.push_str("            let mut j = i + 1;\n");
            out.push_str("            for _ in 0..track_count {\n");
            out.push_str("                if j + 2 > packed.len() { return Err(\"style tag track list data truncated\"); }\n");
            out.push_str("                tracks.push((packed[j], packed[j + 1]));\n");
            out.push_str("                j += 2;\n");
            out.push_str("            }\n");
            out.push_str(&format!(
                "            Ok((StyleTag::{} {{ tracks }}, j))\n",
                variant
            ));
        } else if tag.variable_length {
            // string: [byte_len, byte0, byte1, …] — one UTF-8 byte per f32 slot (wire format)
            out.push_str("            if i >= packed.len() { return Err(\"style tag string truncated\"); }\n");
            out.push_str("            let byte_len = packed[i] as usize;\n");
            out.push_str("            if i + 1 + byte_len > packed.len() { return Err(\"style tag string data truncated\"); }\n");
            out.push_str("            let bytes: Vec<u8> = (0..byte_len).map(|j| packed[i + 1 + j] as u8).collect();\n");
            out.push_str("            let family = String::from_utf8(bytes).map_err(|_| \"invalid utf8 in font family\")?;\n");
            out.push_str(&format!(
                "            Ok((StyleTag::{} {{ family }}, i + 1 + byte_len))\n",
                variant
            ));
        } else if fields.is_empty() {
            out.push_str(&format!("            Ok((StyleTag::{}, i))\n", variant));
        } else {
            out.push_str(&format!(
                "            if i + {} > packed.len() {{ return Err(\"style tag {} truncated\"); }}\n",
                slot_count, tag.name
            ));
            let mut si = 0usize;
            for (fname, _ftype) in &fields {
                out.push_str(&format!(
                    "            let {} = packed[i + {}];\n",
                    fname, si
                ));
                si += 1;
            }
            let field_list: Vec<String> = fields.iter().map(|(n, _)| n.clone()).collect();
            out.push_str(&format!(
                "            Ok((StyleTag::{} {{ {} }}, i + {}))\n",
                variant,
                field_list.join(", "),
                slot_count
            ));
        }
        out.push_str("        }\n");
    }
    out.push_str("        _ => Err(\"unknown style tag\"),\n");
    out.push_str("    }\n");
    out.push_str("}\n\n");

    out.push_str(&generate_style_codec(proto));
    out.push_str(&generate_encode_event(proto));

    out
}

fn generate_style_codec(proto: &Proto) -> String {
    let mut out = String::new();

    out.push_str("// ── Style packet codec (generated) ─────────────────────────────────────\n\n");
    out.push_str("use hayate_core::{\n");
    out.push_str("    AlignContentValue, AlignSelfValue, AlignValue, BorderStyleValue, Color, Dimension, DimensionUnit,\n");
    out.push_str("    DisplayValue,\n");
    out.push_str("    FlexDirectionValue, FlexWrapValue, FontStyleValue, JustifyValue, StyleProp, TextDecorationValue,\n");
    out.push_str("};\n");
    out.push_str("use wasm_bindgen::prelude::*;\n\n");

    out.push_str("fn codec_dim(value: f32, unit_raw: f32) -> Dimension {\n");
    out.push_str("    let unit = match unit_raw as u32 {\n");
    out.push_str("        0 => DimensionUnit::Px,\n");
    out.push_str("        1 => DimensionUnit::Percent,\n");
    out.push_str("        2 => DimensionUnit::Auto,\n");
    out.push_str("        3 => DimensionUnit::Fr,\n");
    out.push_str("        _ => DimensionUnit::Px,\n");
    out.push_str("    };\n");
    out.push_str("    Dimension { value, unit }\n");
    out.push_str("}\n\n");

    out.push_str("fn codec_color(r: f32, g: f32, b: f32, a: f32) -> Color {\n");
    out.push_str("    Color::new(r as f64, g as f64, b as f64, a as f64)\n");
    out.push_str("}\n\n");

    for en in &proto.enums {
        let rust_enum = match en.name.as_str() {
            "display" => "DisplayValue",
            "flex_direction" => "FlexDirectionValue",
            "flex_wrap" => "FlexWrapValue",
            "align_items" => "AlignValue",
            "align_self" => "AlignSelfValue",
            "align_content" => "AlignContentValue",
            "justify_content" => "JustifyValue",
            "font_style" => "FontStyleValue",
            "text_decoration" => "TextDecorationValue",
            "border_style" => "BorderStyleValue",
            _ => continue,
        };
        let fn_name = format!("codec_{}", en.name);
        out.push_str(&format!("fn {fn_name}(raw: f32) -> {rust_enum} {{\n"));
        out.push_str("    match raw as u32 {\n");
        for v in &en.values {
            let variant = to_pascal(&v.name);
            out.push_str(&format!(
                "        {} => {rust_enum}::{variant},\n",
                v.value
            ));
        }
        let default_variant = to_pascal(&en.values[0].name);
        out.push_str(&format!("        _ => {rust_enum}::{default_variant},\n"));
        out.push_str("    }\n");
        out.push_str("}\n\n");
    }

    out.push_str("fn style_tag_to_prop(tag: StyleTag) -> Result<StyleProp, JsValue> {\n");
    out.push_str("    Ok(match tag {\n");
    for tag in &proto.style_tags {
        let variant = to_pascal(&tag.name);
        let prop_variant = to_pascal(&tag.name);
        out.push_str(&format!("        StyleTag::{variant}"));
        if ValueType::classify(tag).is_dimension_list() {
            out.push_str(" { tracks } => ");
        } else {
            let fields = flatten_tag_params(&tag.params, proto);
            if fields.is_empty() {
                out.push_str(" => ");
            } else {
                let field_names: Vec<String> = fields.iter().map(|(n, _)| n.clone()).collect();
                out.push_str(&format!(" {{ {} }} => ", field_names.join(", ")));
            }
        }
        let vt = ValueType::classify(tag);
        let expr = vt.decode_to_prop_expr(&tag.params, proto);
        out.push_str(&format!("StyleProp::{prop_variant}({expr}),\n"));
    }
    out.push_str("    })\n");
    out.push_str("}\n\n");

    out.push_str(
        "pub fn decode_style_packet(packed: &[f32]) -> Result<Vec<StyleProp>, JsValue> {\n",
    );
    out.push_str("    let mut out = Vec::new();\n");
    out.push_str("    let mut i = 0usize;\n");
    out.push_str("    while i < packed.len() {\n");
    out.push_str("        let (tag, next) = parse_next_style_tag(packed, i)\n");
    out.push_str("            .map_err(|e| JsValue::from_str(e))?;\n");
    out.push_str("        i = next;\n");
    out.push_str("        out.push(style_tag_to_prop(tag)?);\n");
    out.push_str("    }\n");
    out.push_str("    Ok(out)\n");
    out.push_str("}\n\n");

    out
}

fn generate_codec(proto: &Proto) -> String {
    let mut out = String::new();
    out.push_str(GENERATED_HEADER);
    out.push_str("// ── Wire codec encode (generated) ────────────────────────────────────────\n\n");

    out.push_str("fn encode_dim_unit(unit: DimensionUnit) -> f32 {\n");
    out.push_str("    match unit {\n");
    out.push_str("        DimensionUnit::Px => 0.0,\n");
    out.push_str("        DimensionUnit::Percent => 1.0,\n");
    out.push_str("        DimensionUnit::Auto => 2.0,\n");
    out.push_str("        DimensionUnit::Fr => 3.0,\n");
    out.push_str("    }\n");
    out.push_str("}\n\n");

    for en in &proto.enums {
        let rust_enum = match en.name.as_str() {
            "display" => "DisplayValue",
            "flex_direction" => "FlexDirectionValue",
            "flex_wrap" => "FlexWrapValue",
            "align_items" => "AlignValue",
            "align_self" => "AlignSelfValue",
            "align_content" => "AlignContentValue",
            "justify_content" => "JustifyValue",
            "font_style" => "FontStyleValue",
            "text_decoration" => "TextDecorationValue",
            "border_style" => "BorderStyleValue",
            _ => continue,
        };
        let fn_name = format!("encode_{}", en.name);
        out.push_str(&format!("fn {fn_name}(value: {rust_enum}) -> f32 {{\n"));
        out.push_str("    match value {\n");
        for v in &en.values {
            let variant = to_pascal(&v.name);
            out.push_str(&format!(
                "        {rust_enum}::{variant} => {}.0,\n",
                v.value
            ));
        }
        out.push_str("    }\n");
        out.push_str("}\n\n");
    }

    out.push_str("pub fn encode_op(buf: &mut Vec<f64>, op: &Op) {\n");
    out.push_str("    match op {\n");
    for op in &proto.opcodes {
        let variant = to_pascal(&op.name);
        if op.params.is_empty() {
            out.push_str(&format!("        Op::{variant} => buf.push(OP_{} as f64),\n", op.name));
            continue;
        }
        let fields: Vec<String> = op.params.iter().map(|p| p.name.clone()).collect();
        out.push_str(&format!(
            "        Op::{variant} {{ {} }} => {{\n",
            fields.join(", ")
        ));
        out.push_str(&format!("            buf.push(OP_{} as f64);\n", op.name));
        for p in &op.params {
            let count = if p.count == 0 { 1 } else { p.count };
            if count > 1 {
                out.push_str(&format!("            for slot in {name}.iter() {{\n", name = p.name));
                out.push_str("                buf.push(*slot);\n");
                out.push_str("            }\n");
            } else {
                let push = match p.typ.as_str() {
                    "element_id" | "u32" | "usize" => format!("buf.push(*{name} as f64);", name = p.name),
                    "f32" => format!("buf.push(*{name} as f64);", name = p.name),
                    "f64" => format!("buf.push(*{name});", name = p.name),
                    "bool" => format!("buf.push(if *{name} {{ 1.0 }} else {{ 0.0 }});", name = p.name),
                    other => panic!("unsupported op param type for encode: {other}"),
                };
                out.push_str(&format!("            {push}\n"));
            }
        }
        out.push_str("        }\n");
    }
    out.push_str("    }\n");
    out.push_str("}\n\n");

    out.push_str("pub fn encode_style_packet(buf: &mut Vec<f32>, props: &[StyleProp]) {\n");
    out.push_str("    for prop in props {\n");
    out.push_str("        match prop {\n");
    for tag in &proto.style_tags {
        let prop_variant = to_pascal(&tag.name);
        let vt = ValueType::classify(tag);
        let binding = vt.encode_binding();
        out.push_str(&format!("            StyleProp::{prop_variant}{binding} => {{\n"));
        out.push_str(&format!("                buf.push(TAG_{} as f32);\n", tag.name));
        out.push_str(&vt.encode_body());
        out.push_str("            }\n");
    }
    out.push_str("        }\n");
    out.push_str("    }\n");
    out.push_str("}\n");

    out
}

