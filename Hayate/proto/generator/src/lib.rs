fn generate_event_types(proto: &Proto) -> String {
    let mut out = String::new();
    out.push_str(GENERATED_HEADER);
    out.push_str("use super::id::ElementId;\n\n");

    out.push_str("/// Semantic interaction events (ADR-0031).\n");
    out.push_str("#[derive(Clone, Debug)]\n");
    out.push_str("pub enum Event {\n");
    for ev in &proto.event_kinds {
        if let Some(desc) = &ev.description {
            for line in desc.lines() {
                out.push_str(&format!("    /// {}\n", line));
            }
        }
        let variant = to_pascal(&ev.name);
        if ev.params.is_empty() {
            out.push_str(&format!("    {},\n", variant));
        } else {
            out.push_str(&format!("    {} {{\n", variant));
            for p in &ev.params {
                out.push_str(&format!(
                    "        {}: {},\n",
                    p.name,
                    event_field_rust_type(&p.typ)
                ));
            }
            out.push_str("    },\n");
        }
    }
    out.push_str("}\n\n");

    let doc_events: Vec<_> = proto
        .event_kinds
        .iter()
        .filter(|ev| ev.document_runtime)
        .collect();

    out.push_str("/// Event kinds registerable via `register_listener`.\n");
    out.push_str("#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]\n");
    out.push_str("pub enum DocumentEventKind {\n");
    for ev in &doc_events {
        out.push_str(&format!("    {},\n", to_pascal(&ev.name)));
    }
    out.push_str("}\n\n");

    out.push_str("impl DocumentEventKind {\n");
    out.push_str("    pub fn bubbles(self) -> bool {\n");
    out.push_str("        match self {\n");
    for ev in &doc_events {
        let arm = if ev.bubbles { "true" } else { "false" };
        out.push_str(&format!(
            "            Self::{} => {},\n",
            to_pascal(&ev.name),
            arm
        ));
    }
    out.push_str("        }\n");
    out.push_str("    }\n\n");

    out.push_str("    pub fn from_u32(value: u32) -> Option<Self> {\n");
    out.push_str("        match value {\n");
    for ev in &doc_events {
        out.push_str(&format!(
            "            {} => Some(Self::{}),\n",
            ev.value,
            to_pascal(&ev.name)
        ));
    }
    out.push_str("            _ => None,\n");
    out.push_str("        }\n");
    out.push_str("    }\n");
    out.push_str("}\n\n");

    out.push_str("/// Map a runtime `Event` to its document kind, if registerable.\n");
    out.push_str("pub fn event_document_kind(event: &Event) -> Option<DocumentEventKind> {\n");
    out.push_str("    match event {\n");
    for ev in &doc_events {
        out.push_str(&format!(
            "        {} => Some(DocumentEventKind::{}),\n",
            event_match_wildcard(ev),
            to_pascal(&ev.name)
        ));
    }
    out.push_str("        _ => None,\n");
    out.push_str("    }\n");
    out.push_str("}\n");

    out
}

fn generate_pseudo_state_tables(proto: &Proto) -> String {
    let mut out = String::new();
    out.push_str(GENERATED_HEADER);
    out.push_str("use super::PseudoState;\n\n");

    out.push_str("pub fn pseudo_state_from_u32(v: u32) -> Option<PseudoState> {\n");
    out.push_str("    match v {\n");
    for ps in &proto.pseudo_states {
        out.push_str(&format!(
            "        {} => Some(PseudoState::{}),\n",
            ps.value,
            to_pascal(&ps.name)
        ));
    }
    out.push_str("        _ => None,\n");
    out.push_str("    }\n");
    out.push_str("}\n\n");

    let mut resolve_order: Vec<&PseudoStateEntry> = proto.pseudo_states.iter().collect();
    resolve_order.sort_by_key(|ps| ps.priority);

    out.push_str("/// Application order for `resolve_visual` (ascending priority; last wins).\n");
    out.push_str("pub const PSEUDO_RESOLVE_ORDER: [PseudoState; ");
    out.push_str(&resolve_order.len().to_string());
    out.push_str("] = [\n");
    for ps in &resolve_order {
        out.push_str(&format!("    PseudoState::{},\n", to_pascal(&ps.name)));
    }
    out.push_str("];\n\n");

    out.push_str("pub fn pseudo_state_css_suffix(state: PseudoState) -> &'static str {\n");
    out.push_str("    match state {\n");
    for ps in &proto.pseudo_states {
        out.push_str(&format!(
            "        PseudoState::{} => \"{}\",\n",
            to_pascal(&ps.name),
            ps.css_pseudo
        ));
    }
    out.push_str("    }\n");
    out.push_str("}\n\n");

    out.push_str("pub fn pseudo_state_css_priority(state: PseudoState) -> u32 {\n");
    out.push_str("    match state {\n");
    for ps in &proto.pseudo_states {
        out.push_str(&format!(
            "        PseudoState::{} => {},\n",
            to_pascal(&ps.name),
            ps.priority
        ));
    }
    out.push_str("    }\n");
    out.push_str("}\n");

    out
}

