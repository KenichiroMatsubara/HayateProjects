#[derive(Deserialize)]
struct ParamJson {
    name: String,
    #[serde(rename = "type")]
    typ: String,
    #[serde(default)]
    count: usize,
}

#[derive(Deserialize)]
struct EntryJson {
    name: String,
    value: u32,
    #[serde(default)]
    variable_length: bool,
    #[serde(default)]
    params: Vec<ParamJson>,
    #[serde(default, rename = "encodeFrom")]
    encode_from: Option<String>,
    #[serde(default, rename = "wireRole")]
    wire_role: Option<String>,
    #[serde(default, rename = "adapterTier")]
    adapter_tier: Option<String>,
    #[serde(default, rename = "documentRuntime")]
    document_runtime: bool,
    #[serde(default)]
    bubbles: bool,
    #[serde(default, rename = "interactionKind")]
    interaction_kind: Option<String>,
    #[serde(default)]
    description: Option<String>,
    #[serde(default, rename = "domCss")]
    dom_css: Option<DomCssJson>,
}

#[derive(Deserialize)]
struct DomCssJson {
    property: String,
    format: String,
    #[serde(default)]
    extras: Vec<DomExtraJson>,
}

#[derive(Deserialize)]
struct DomExtraJson {
    property: String,
    #[serde(rename = "whenPositive")]
    when_positive: String,
    #[serde(rename = "whenZero")]
    when_zero: String,
}

#[derive(Deserialize)]
struct TypeJson {
    name: String,
    raw_slots: usize,
    fields: Vec<ParamJson>,
}

#[derive(Deserialize)]
struct EnumValueJson {
    name: String,
    value: serde_json::Value,
}

#[derive(Deserialize)]
struct EnumJson {
    name: String,
    #[serde(default)]
    string_values: bool,
    values: Vec<EnumValueJson>,
}

#[derive(Deserialize)]
struct SimpleJson {
    name: String,
    value: u32,
}

#[derive(Deserialize)]
struct PseudoStateJson {
    name: String,
    value: u32,
    priority: u32,
    #[serde(rename = "cssPseudo")]
    css_pseudo: String,
}

fn read_json<T: for<'de> Deserialize<'de>>(spec_dir: &Path, name: &str) -> T {
    let path = spec_dir.join(name);
    let text = fs::read_to_string(&path)
        .unwrap_or_else(|e| panic!("cannot read {}: {}", path.display(), e));
    serde_json::from_str(&text)
        .unwrap_or_else(|e| panic!("invalid JSON in {}: {}", path.display(), e))
}

fn enum_value_string(value: &serde_json::Value) -> String {
    match value {
        serde_json::Value::Number(n) => n.to_string(),
        serde_json::Value::String(s) => s.clone(),
        other => panic!("invalid enum value: {other}"),
    }
}

fn load_spec(spec_dir: &Path) -> Proto {
    let types: Vec<TypeJson> = read_json(spec_dir, "types.json");
    let enums: Vec<EnumJson> = read_json(spec_dir, "enums.json");
    let opcodes: Vec<EntryJson> = read_json(spec_dir, "opcodes.json");
    let style_tags: Vec<EntryJson> = read_json(spec_dir, "style_tags.json");
    let event_kinds: Vec<EntryJson> = read_json(spec_dir, "event_kinds.json");
    let element_kinds: Vec<SimpleJson> = read_json(spec_dir, "element_kinds.json");
    let unset_kinds: Vec<SimpleJson> = read_json(spec_dir, "unset_kinds.json");
    let modifier_keys: Vec<SimpleJson> = read_json(spec_dir, "modifier_keys.json");
    let pseudo_states: Vec<PseudoStateJson> = read_json(spec_dir, "pseudo_states.json");

    Proto {
        types: types
            .into_iter()
            .map(|t| TypeDef {
                name: t.name,
                raw_slots: t.raw_slots,
                fields: t
                    .fields
                    .into_iter()
                    .map(|f| Param {
                        name: f.name,
                        typ: f.typ,
                        count: f.count,
                    })
                    .collect(),
            })
            .collect(),
        enums: enums
            .into_iter()
            .map(|e| EnumDef {
                name: e.name,
                string_values: e.string_values,
                values: e
                    .values
                    .into_iter()
                    .map(|v| EnumValue {
                        name: v.name,
                        value: enum_value_string(&v.value),
                    })
                    .collect(),
            })
            .collect(),
        opcodes: entries_from_json(opcodes),
        style_tags: entries_from_json(style_tags),
        event_kinds: entries_from_json(event_kinds),
        element_kinds: simple_from_json(element_kinds),
        unset_kinds: simple_from_json(unset_kinds),
        modifier_keys: simple_from_json(modifier_keys),
        pseudo_states: pseudo_states
            .into_iter()
            .map(|e| PseudoStateEntry {
                name: e.name,
                value: e.value,
                priority: e.priority,
                css_pseudo: e.css_pseudo,
            })
            .collect(),
    }
}

fn entries_from_json(entries: Vec<EntryJson>) -> Vec<Entry> {
    entries
        .into_iter()
        .map(|e| Entry {
            name: e.name,
            value: e.value,
            variable_length: e.variable_length,
            params: e
                .params
                .into_iter()
                .map(|p| Param {
                    name: p.name,
                    typ: p.typ,
                    count: p.count,
                })
                .collect(),
            encode_from: e.encode_from,
            wire_role: e.wire_role,
            adapter_tier: e.adapter_tier,
            document_runtime: e.document_runtime,
            bubbles: e.bubbles,
            interaction_kind: e.interaction_kind,
            description: e.description,
            dom_css: e.dom_css.map(|d| DomCss {
                property: d.property,
                format: d.format,
                extras: d
                    .extras
                    .into_iter()
                    .map(|x| DomExtra {
                        property: x.property,
                        when_positive: x.when_positive,
                        when_zero: x.when_zero,
                    })
                    .collect(),
            }),
        })
        .collect()
}

fn simple_from_json(entries: Vec<SimpleJson>) -> Vec<SimpleEntry> {
    entries
        .into_iter()
        .map(|e| SimpleEntry {
            name: e.name,
            value: e.value,
        })
        .collect()
}

