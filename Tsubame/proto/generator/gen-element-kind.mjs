import { writeFileSync, mkdirSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';
import { loadProtocolSpec } from '@hayate/protocol-spec/load';

const outDir = join(
  dirname(fileURLToPath(import.meta.url)),
  '../../packages/renderer-protocol/src/generated',
);
const outPath = join(outDir, 'element-kind.ts');

/** Spec element-kind name (snake_case) → Tsubame ElementKind union (kebab-case). */
function kindToUnion(name) {
  return name.replace(/_/g, '-');
}

/** Spec cursor enum value (snake_case) → CSS cursor keyword (kebab-case). */
function cursorToCss(name) {
  return name.replace(/_/g, '-');
}

export function generateElementKind() {
  const proto = loadProtocolSpec();

  const entries = (proto.element_kinds ?? [])
    .filter((k) => k.defaultCursor)
    .map((k) => [kindToUnion(k.name), cursorToCss(k.defaultCursor)]);
  const defaults = Object.fromEntries(entries);

  const lines = [
    '// AUTO-GENERATED by Tsubame/proto/generator — DO NOT EDIT',
    '// Source: @hayate/protocol-spec (element_kinds.defaultCursor)',
    '',
    "import type { ElementKind } from '../element.js';",
    '',
    '/** UA default cursor (CSS keyword) per element-kind (ADR-0105). Single source',
    ' *  shared with Canvas (Hayate core `resolve_cursor`); absent = no default. */',
    `const DEFAULT_CURSOR: Partial<Record<ElementKind, string>> = ${JSON.stringify(defaults)};`,
    '',
    '/** UA default cursor for `kind` when no explicit `cursor` is set, else undefined. */',
    'export function elementKindDefaultCursor(kind: ElementKind): string | undefined {',
    '  return DEFAULT_CURSOR[kind];',
    '}',
    '',
  ];

  mkdirSync(outDir, { recursive: true });
  writeFileSync(outPath, lines.join('\n'), 'utf8');
}
