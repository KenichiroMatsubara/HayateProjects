import { writeFileSync, mkdirSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';
import { loadProtocolSpec, toCamelCase } from '@hayate/protocol-spec/load';

const outPath = join(dirname(fileURLToPath(import.meta.url)), '../generated/delivery.ts');

/** Hayate wire event name → Tsubame {@link EventKind} (adapter vocabulary). */
const WIRE_TO_EVENT_KIND = {
  click: 'click',
  focus: 'focus',
  blur: 'blur',
  text_input: 'input',
  hover_enter: 'hover-enter',
  hover_leave: 'hover-leave',
  key_down: 'keydown',
};

export function generateDelivery() {
  const proto = loadProtocolSpec();
  const events = proto.event_kinds ?? [];

  const listenerEntries = events.filter(
    (ev) => ev.adapterTier === 'forward' && WIRE_TO_EVENT_KIND[ev.name] !== undefined,
  );

  /** Wire events registerable via Hayate but not mapped to InteractionEvent (adapterTier: deferred). */
  const deferredEntries = events.filter(
    (ev) =>
      ev.adapterTier === 'deferred' &&
      ev.wireRole !== 'hayate-internal' &&
      ev.wireRole !== 'host-echo' &&
      ev.name !== 'pointer_move',
  );

  const ignoredNames = events
    .filter(
      (ev) =>
        ev.adapterTier !== 'forward' ||
        ev.wireRole === 'hayate-internal' ||
        ev.wireRole === 'host-echo' ||
        WIRE_TO_EVENT_KIND[ev.name] === undefined,
    )
    .map((ev) => `'${ev.name}'`);

  const listenerLines = listenerEntries.map((ev) => {
    const kind = WIRE_TO_EVENT_KIND[ev.name];
    const constKey = ev.name.toUpperCase();
    return `  '${kind}': EVENT_KIND.${constKey},`;
  });

  const deferredLines = deferredEntries.map((ev) => {
    const constKey = ev.name.toUpperCase();
    return `  '${ev.name}': EVENT_KIND.${constKey},`;
  });

  const switchCases = listenerEntries.map((ev) => {
    const kind = WIRE_TO_EVENT_KIND[ev.name];
    const fields = [`kind: '${kind}'`, 'target: asElementId(ev.targetId)'];
    if (ev.name === 'text_input') fields.push('value: ev.text');
    if (ev.name === 'key_down') fields.push('key: ev.key');
    return `    case '${ev.name}':\n      return { ${fields.join(', ')} };`;
  });

  const lines = [
    '// AUTO-GENERATED by Tsubame/proto/generator — DO NOT EDIT',
    '// Source: @hayate/protocol-spec (event_kinds wireRole / adapterTier)',
    '',
    "import type { EventKind, InteractionEvent } from '@tsubame/renderer-protocol';",
    "import { asElementId } from '@tsubame/renderer-protocol';",
    "import { EVENT_KIND, type EventPayload, parseEvent } from './protocol.js';",
    '',
    '/** EventKinds registerable via Hayate `register_listener` (adapterTier: forward). */',
    'export const HAYATE_LISTENER_KIND: Partial<Record<EventKind, number>> = {',
    ...listenerLines,
    '};',
    '',
    '/** Hayate wire kinds with adapterTier deferred (scroll, composition_*, …). */',
    'export const HAYATE_DEFERRED_LISTENER_KIND: Readonly<Record<string, number>> = {',
    ...deferredLines,
    '};',
    '',
    'const IGNORED_KINDS: ReadonlySet<EventPayload[\'kind\']> = new Set([',
    ...ignoredNames.map((n) => `  ${n},`),
    ']);',
    '',
    'export interface EventDelivery {',
    '  listenerId: number;',
    '  event: EventPayload;',
    '}',
    '',
    '/** Decode one Hayate `poll_events()` delivery row: `[listener_id, kind, ...fields]`. */',
    'export function parseDelivery(row: unknown[]): EventDelivery {',
    '  const listenerId = row[0] as number;',
    '  const event = parseEvent(row.slice(1) as unknown[]);',
    '  return { listenerId, event };',
    '}',
    '',
    '/** Map a parsed Hayate event payload to a Tsubame {@link InteractionEvent}, if deliverable. */',
    'export function toInteractionEvent(ev: EventPayload): InteractionEvent | null {',
    '  if (IGNORED_KINDS.has(ev.kind)) return null;',
    '',
    '  switch (ev.kind) {',
    ...switchCases,
    '    default:',
    '      return null;',
    '  }',
    '}',
    '',
  ];

  mkdirSync(dirname(outPath), { recursive: true });
  writeFileSync(outPath, lines.join('\n'), 'utf8');
}
